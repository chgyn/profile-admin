<script>
export default {
    data() {
      return {
        errors: {},
        email: '',
        assunto: '',
        mensagem: '',
		nome: '',
		has_error: false,
        has_success: false,
        show: false,
      }
    },
    components: {
	},
    mounted() {
    },
    methods:{
		enviarMensagem(){
            this.show = true
			var app = this
            axios.post('api/msg', {
				nome: app.nome,
				email: app.email,
                assunto: app.assunto,
                mensagem: app.mensagem,
			})
            .then(response=>{		
               
                this.show = false;

                this.has_success = true;
                this.email = '';
                this.mensagem = '';
                this.assunto = '';
                this.nome = '';
                this.errors = {};

            }, error => {
                switch(error.response.status){
                    case 422:
                        this.show = false;
                        this.errors=error.response.data.errors;
                        break;
                    default:
                        this.has_error = true;
                        this.show = false;
                        break;
                }
            })       
        },
    } 
